def make_readable(seconds):
    HH = seconds // 3600
    MM = (seconds % 3600) // 60
    SS = seconds % 60
    return f'{HH:02d}:{MM:02d}:{SS:02d}'

test = make_readable(1000000)  # Пример для значения больше 359999
print(test)
