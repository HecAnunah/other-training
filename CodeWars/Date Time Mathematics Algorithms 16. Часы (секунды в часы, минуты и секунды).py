# Напишите функцию, которая принимает неотрицательное целое число (секунды)
# в качестве входного и возвращает время в формате читаемого человека (HH: MM: SS)
# HH = часы, мягкие до 2 цифр, диапазон: 00 - 99
# ММ = минуты, пролаженные до 2 цифр, диапазон: 00 - 59
# SS = секунды, пролаженные до 2 цифр, диапазон: 00 - 59
# Максимальное время никогда не превышает 359999 (99:59:59)

def make_readable(seconds):
    HH = seconds // 3600
    MM = (seconds - HH * 3600) // 60
    SS = seconds - ((HH * 3600) + (MM * 60))
    return f'{HH:02d}:{MM:02d}:{SS:02d}'

test = make_readable(359999)
print(test)

